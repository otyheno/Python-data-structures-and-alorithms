#Test whether an item is or is not in a sequence. Returns true or false

#string
name = 'Henry'
print ('H' not in name) #the output is false

#list
animals = ['Goat', 'Donkey', 'Sheep', 'Cow']
print('Sheep' in animals) #the output is true

#tuple
students = ('Anne', 'Juatine', 'Yvonne', 'Samwel')
print("Samwel" not in students) #the output is False