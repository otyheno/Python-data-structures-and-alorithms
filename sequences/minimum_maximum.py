# find the minimum or maximum item in a sequence lexicographically.
#alpha or numeric types but cannot be mixed

#string
name = 'Henry'
print (min(name)) #the output is H
print (max(name)) #the output is y

#list
animals = ['Goat', 'Donkey', 'Sheep', 'Cow']
print(min(animals)) #the output is Cow
print(max(animals)) #the output is Sheep

#tuple
students = ('Anne', 'Justine', 'Yvonne', 'Samwel')
print(min(students)) #the output is Anne
print(max(students)) #the output is Yvonne